    // Nút thêm
    private void jlbAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbAddMouseClicked
        String maKH = jTextMaKH.getText();
        String tenKH = jTextTenKH.getText();
        Date ngaySinh = jDateNgaySinh.getDate();
        String gioiTinh = (String) jCbGioiTinh.getSelectedItem();
        String diaChi = jTextDiaChi.getText();
        String dienThoai = jTextDienThoai.getText();
        String email = jTextEmail.getText();
        String currentEmail = khachHangController.getCurrentEmail();
        String loaiKH = (String) jCbLoaiKH.getSelectedItem();

        // Kiểm tra dữ liệu đã nhập đủ chưa
        if (!DataValidator.isValidDataKH(maKH, tenKH, ngaySinh, diaChi, dienThoai, email)) {
            return;
        }

        // Kiểm tra tên khách hàng
        if (!DataValidator.isValidTenKH(tenKH)) {
            return;
        }

        // Kiểm tra số điện thoại
        if (!DataValidator.isValidSoDienThoai(dienThoai)) {
            return;
        }

        // Kiểm tra email duy nhất
        if (!khachHangController.isValidEmail(email, currentEmail)) {
            return;
        }

        // Kiểm tra mã khách hàng có bị trùng 
        try {
            if (khachHangController.isMaKHExist(maKH)) {
                JOptionPane.showMessageDialog(this, "Customer ID already exists. Please enter a different Customer ID");
                return; 
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(KhachHangJPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error checking customer ID");
            return; // Dừng thực hiện nếu có lỗi
        }

        KhachHang newKhachHang = new KhachHang(maKH, tenKH, new java.sql.Date(ngaySinh.getTime()), gioiTinh, diaChi, dienThoai, email, loaiKH);

        // Thêm khách hàng mới
        khachHangController.addKhachHang(newKhachHang);
        JOptionPane.showMessageDialog(this, "Added successful");

        try {
            // Làm mới bảng
            loadKhachHangData();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(KhachHangJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Xoá các trường dữ liệu
        clearFormField();
    }//GEN-LAST:event_jlbAddMouseClicked

    // Chọn dòng trên bảng
    private void jTableKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableKhachHangMouseClicked
        // Lấy dòng đã chọn
        int selectedRow = jTableKhachHang.getSelectedRow();

        // Kiểm tra dòng đã được chọn
        if (selectedRow != -1) {
            // Lấy dữ liệu từ dòng đã chọn và đưa vào các ô nhập liệu
            String maKhachHang = (String) jTableKhachHang.getValueAt(selectedRow, 0);
            String tenKhachHang = (String) jTableKhachHang.getValueAt(selectedRow, 1);
            Date ngaySinh = (Date) jTableKhachHang.getValueAt(selectedRow, 2);
            String gioiTinh = (String) jTableKhachHang.getValueAt(selectedRow, 3);
            String diaChi = (String) jTableKhachHang.getValueAt(selectedRow, 4);
            String soDienThoai = (String) jTableKhachHang.getValueAt(selectedRow, 5);
            String email = (String) jTableKhachHang.getValueAt(selectedRow, 6);
            String loaiKhachHang = (String) jTableKhachHang.getValueAt(selectedRow, 7);

            // Đưa dữ liệu vào các ô nhập liệu
            jTextMaKH.setText(maKhachHang);
            jTextTenKH.setText(tenKhachHang);
            jDateNgaySinh.setDate(ngaySinh);
            jCbGioiTinh.setSelectedItem(gioiTinh);
            jTextDiaChi.setText(diaChi);
            jTextDienThoai.setText(soDienThoai);
            jTextEmail.setText(email);
            jCbLoaiKH.setSelectedItem(loaiKhachHang);

            // Ngăn user chỉnh sửa MAKH
            jTextMaKH.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent evt) {
                    int selectedRow = jTableKhachHang.getSelectedRow();
                    if (selectedRow != -1) {
                        // Hiển thị thông báo nếu hàng đã được chọn
                        JOptionPane.showMessageDialog(null, "You do not have permission to edit the customer ID", "Warning", JOptionPane.WARNING_MESSAGE);
                        jTextMaKH.setText(maKhachHang);
                        jTextMaKH.setEditable(false);
                    }
                }
            });

        }
    }//GEN-LAST:event_jTableKhachHangMouseClicked


