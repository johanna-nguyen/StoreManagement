    // Load bảng sản phầm
    public void loadKhachHangData() throws ClassNotFoundException {
        List<KhachHang> khachHangList = khachHangController.getAllKhachHang();
        DefaultTableModel model = (DefaultTableModel) jTableKhachHang.getModel();
        model.setRowCount(0);

        for (KhachHang kh : khachHangList) {
            model.addRow(new Object[]{
                kh.getMaKh(),
                kh.getTenKh(),
                kh.getNgaySinh(),
                kh.getGioiTinh(),
                kh.getDiaChi(),
                kh.getDienThoai(),
                kh.getEmail(),
                kh.getLoaiKh()
            });
        }

    }

