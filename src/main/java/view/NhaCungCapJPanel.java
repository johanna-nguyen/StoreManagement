    // Load bảng ncc
    public void loadNhaCungCapData() throws ClassNotFoundException {
        List<NhaCungCap> nhaCCList = nhaCCController.getAllNhaCungCap();
        DefaultTableModel model = (DefaultTableModel) jTableNhaCungCap.getModel();
        model.setRowCount(0); // Clear existing rows

        if (nhaCCList != null) {
            for (NhaCungCap ncc : nhaCCList) {
                model.addRow(new Object[]{
                    ncc.getMaNCC(),
                    ncc.getTenNCC(),
                    ncc.getDiaChi(),
                    ncc.getDienThoai(),
                    ncc.getEmail(),
                    ncc.getWebsite(),
                    ncc.getGhiChu()
                });
            }
        }

    }

