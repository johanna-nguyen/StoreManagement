    // Nút sửa
    private void jlbUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbUpdateMouseClicked
        String maNCC = jTextMaNCC.getText();
        String tenNCC = jTextTenNCC.getText();
        String diaChi = jTextDiaChi.getText();
        String dienThoai = jTextDienThoai.getText();
        String email = jTextEmail.getText();
        String website = jTextWebsite.getText();
        String ghiChu = jTextGhiChu.getText();
        String currentEmail = nhaCCController.getCurrentEmail();

        // Kiểm tra dữ liệu đã nhập đủ chưa
        if (!DataValidator.isValidDataNCC(maNCC, tenNCC, diaChi, dienThoai, email)) {
            return;
        }

        // Kiểm tra số điện thoại
        if (!DataValidator.isValidSoDienThoai(dienThoai)) {
            return;
        }

        // Kiểm tra email duy nhất
        if (!nhaCCController.isValidEmail(email, currentEmail)) {
            return;
        }

        // Cập nhật thông tin NCC
        try {
            nhaCCController.updateNhaCungCap();
            clearFormField();

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NhaCungCapJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jlbUpdateMouseClicked

