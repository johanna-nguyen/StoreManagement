    private void jTableNhaCungCapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNhaCungCapMouseClicked
        // Lấy dòng đã chọn
        int selectedRow = jTableNhaCungCap.getSelectedRow();

        // Kiểm tra dòng đã được chọn
        if (selectedRow != -1) {
            // Lấy dữ liệu từ dòng đã chọn và đưa vào các ô nhập liệu
            String maNCC = (String) jTableNhaCungCap.getValueAt(selectedRow, 0);
            String tenNCC = (String) jTableNhaCungCap.getValueAt(selectedRow, 1);
            String diaChi = (String) jTableNhaCungCap.getValueAt(selectedRow, 2);
            String soDienThoai = (String) jTableNhaCungCap.getValueAt(selectedRow, 3);
            String email = (String) jTableNhaCungCap.getValueAt(selectedRow, 4);
            String website = (String) jTableNhaCungCap.getValueAt(selectedRow, 5);
            String ghiChu = (String) jTableNhaCungCap.getValueAt(selectedRow, 6);

            // Đưa dữ liệu vào các ô nhập liệu
            jTextMaNCC.setText(maNCC);
            jTextTenNCC.setText(tenNCC);
            jTextDiaChi.setText(diaChi);
            jTextDienThoai.setText(soDienThoai);
            jTextEmail.setText(email);
            jTextWebsite.setText(website);
            jTextGhiChu.setText(ghiChu);

            // Ngăn user chỉnh sửa MANCC
            jTextMaNCC.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent evt) {
                    int selectedRow = jTableNhaCungCap.getSelectedRow();
                    if (selectedRow != -1) {
                        // Hiển thị thông báo nếu hàng đã được chọn
                        JOptionPane.showMessageDialog(null, "You do not have permission to edit the supplier ID", "Warning", JOptionPane.WARNING_MESSAGE);
                        jTextMaNCC.setText(maNCC);
                        jTextMaNCC.setEditable(false);
                    }
                }
            });

        }
    }//GEN-LAST:event_jTableNhaCungCapMouseClicked

