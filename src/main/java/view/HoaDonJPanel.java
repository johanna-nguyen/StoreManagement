    // Nút thêm
    private void jlbAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbAddMouseClicked
        String maHD = jTextMaHD.getText();
        String currentMaHD = hoaDonController.getCurrentMaHD();
        Date ngayHD = jDateNgayHD.getDate();
        String maKH = (String) jCbMaKH.getSelectedItem();
        String maNV = (String) jCbMaNV.getSelectedItem();

        BigDecimal triGia = null;
        triGia = new BigDecimal(jTextTriGia.getText());

        // Kiểm tra dữ liệu đã nhập đủ chưa
        DataValidator.isValidDataHD(maHD, ngayHD, maKH, maNV, triGia);

        // Kiểm tra mã hd có bị trùng không
        try {
            if (hoaDonController.isMaHDExist(maHD, currentMaHD)) {
                JOptionPane.showMessageDialog(this, "Purchase Order ID already exists. Please enter a different ID");
                return; // Dừng thực hiện nếu mã đã tồn tại
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(HoaDonJPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error checking Invoice ID");
            return; // Dừng thực hiện nếu có lỗi
        }

        HoaDon newHoaDon = new HoaDon(maHD, new java.sql.Date(ngayHD.getTime()), maKH, maNV, triGia);

        // Thêm hd mới
        try {
            hoaDonController.addHoaDon(newHoaDon);
            JOptionPane.showMessageDialog(this, "Added successful!");

            // Làm mới bảng và hiển thị lại dữ liệu
            loadHoaDonData();

            // Xóa các trường dữ liệu
            clearFormField();

        } catch (HeadlessException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Error adding invoice: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(HoaDonJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jlbAddMouseClicked

    // Chọn dòng trên bảng
    private void jTableHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableHoaDonMouseClicked
        // Lấy dòng đã chọn
        int selectedRow = jTableHoaDon.getSelectedRow();

        // Kiểm tra dòng đã được chọn
        if (selectedRow != -1) {
            // Lấy dữ liệu từ dòng đã chọn và đưa vào các ô nhập liệu
            String maHD = (String) jTableHoaDon.getValueAt(selectedRow, 0);
            Date ngayHD = (Date) jTableHoaDon.getValueAt(selectedRow, 1);
            String maKH = (String) jTableHoaDon.getValueAt(selectedRow, 2);
            String maNV = (String) jTableHoaDon.getValueAt(selectedRow, 3);

            // Chuyển từ kiểu BigDecimal sang String
            BigDecimal triGiaValue = (BigDecimal) jTableHoaDon.getValueAt(selectedRow, 4);
            String triGia = triGiaValue.toString();

            // Đưa dữ liệu vào các ô nhập liệu
            jTextMaHD.setText(maHD);
            jDateNgayHD.setDate(ngayHD);
            jCbMaKH.setSelectedItem(maKH);
            jCbMaNV.setSelectedItem(maNV);
            jTextTriGia.setText(triGia);

            // ngăn không chỉnh maHD
            jTextMaHD.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent evt) {
                    int selectedRow = jTableHoaDon.getSelectedRow();
                    if (selectedRow != -1) {
                        // Hiển thị thông báo nếu hàng đã được chọn
                        JOptionPane.showMessageDialog(null, "You do not have permission to edit the invoice ID", "Warning", JOptionPane.WARNING_MESSAGE);
                        jTextMaHD.setText(maHD);
                        jTextMaHD.setEditable(false);
                    }
                }
            });

        }
    }//GEN-LAST:event_jTableHoaDonMouseClicked

