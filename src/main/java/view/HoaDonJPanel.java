    // Load bảng sản phầm
    public void loadHoaDonData() throws ClassNotFoundException {
        List<HoaDon> hoaDonList = hoaDonController.getAllHoaDon();
        DefaultTableModel model = (DefaultTableModel) jTableHoaDon.getModel();
        model.setRowCount(0);

        for (HoaDon hd : hoaDonList) {
            model.addRow(new Object[]{
                hd.getMaHD(),
                hd.getNgayHD(),
                hd.getMaKH(),
                hd.getMaNV(),
                hd.getTriGia()
            });
        }

    }

