    // Nút thêm
    private void jlbAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbAddMouseClicked
        String maNhap = jTextMaNhap.getText();
        String currentMaNhap = phieuNhapController.getCurrentMaNhap();
        String maSP = (String) jCbMaSPNhap.getSelectedItem();

        int soLuong = 0;
        soLuong = Integer.parseInt(jTextSoLuongNhap.getText());

        BigDecimal giaNhap = null;
        giaNhap = new BigDecimal(jTextGiaNhap.getText());

        Date ngayNhap = jDateNgayNhap.getDate();

        String maNCC = (String) jCbMaNCC.getSelectedItem();

        // Kiểm tra dữ liệu đã nhập đủ chưa
        DataValidator.isValidDataPhieuNhap(maNhap, maSP, soLuong, giaNhap, ngayNhap, maNCC);

        // Kiểm tra mã phiếu nhập có bị trùng không
        try {
            if (phieuNhapController.isMaNhapExist(maNhap, currentMaNhap)) {
                JOptionPane.showMessageDialog(this, "Purchase Order ID already exists. Please enter a different ID");
                return; // Dừng thực hiện nếu mã đã tồn tại
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(PhieuNhapJPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error checking Purchase Order ID");
            return; 
        }

        PhieuNhap newPhieuNhap = new PhieuNhap(maNhap, maSP, soLuong, giaNhap, new java.sql.Date(ngayNhap.getTime()), maNCC);

        // Thêm phiếu nhập mới
        try {
            phieuNhapController.addPhieuNhap(newPhieuNhap);
            JOptionPane.showMessageDialog(this, "Added successful!");

            // Làm mới bảng và hiển thị lại dữ liệu
            loadPhieuNhapData();

            // Xóa các trường dữ liệu
            clearFormField();

        } catch (HeadlessException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Failed to add the invoice.: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(PhieuNhapJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jlbAddMouseClicked


