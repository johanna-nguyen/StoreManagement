    // Nút xoá
    private void jlbDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbDeleteMouseClicked
        // Chọn dòng xoá
        int selectedRow = jTablePhieuNhap.getSelectedRow();

        // Kiểm tra người dùng đã chọn chưa
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to update", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Lấy MANHAP từ dòng muốn xoá
        String maNhap = jTablePhieuNhap.getValueAt(selectedRow, 0).toString();

        // Xác nhận xoá
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?", "Notification", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                PhieuNhap phieuNhap = new PhieuNhap(maNhap, null, 0, BigDecimal.ZERO, null, null);

                // Gọi phương thức xoá
                phieuNhapController.deletePhieuNhap(phieuNhap);

                // Hiển thị thông báo xoá thành
                JOptionPane.showMessageDialog(this, "Deleted successful");

                // Làm mới bảng
                loadPhieuNhapData();
            } catch (HeadlessException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(this, ": " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(PhieuNhapJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jlbDeleteMouseClicked

