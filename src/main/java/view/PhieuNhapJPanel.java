    private void jTablePhieuNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePhieuNhapMouseClicked
        // Lấy dòng đã chọn
        int selectedRow = jTablePhieuNhap.getSelectedRow();

        // Kiểm tra dòng đã được chọn
        if (selectedRow != -1) {
            // Lấy dữ liệu từ dòng đã chọn và đưa vào các ô nhập liệu
            String maNhap = (String) jTablePhieuNhap.getValueAt(selectedRow, 0);
            String maSP = (String) jTablePhieuNhap.getValueAt(selectedRow, 1);

            Integer soLuongValue = (Integer) jTablePhieuNhap.getValueAt(selectedRow, 2);
            String soLuong = soLuongValue.toString();

            // Chuyển từ kiểu BigDecimal sang String
            BigDecimal giaNhapValue = (BigDecimal) jTablePhieuNhap.getValueAt(selectedRow, 3);
            String giaNhap = giaNhapValue.toString();

            Date ngayNhap = (Date) jTablePhieuNhap.getValueAt(selectedRow, 4);
            String maNCC = (String) jTablePhieuNhap.getValueAt(selectedRow, 5);

            // Đưa dữ liệu vào các ô nhập liệu
            jTextMaNhap.setText(maNhap);
            jCbMaSPNhap.setSelectedItem(maSP);
            jTextSoLuongNhap.setText(soLuong);
            jTextGiaNhap.setText(giaNhap);
            jDateNgayNhap.setDate(ngayNhap);
            jCbMaNCC.setSelectedItem(maNCC);

            // ngăn không chỉnh maNhap
            jTextMaNhap.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent evt) {
                    int selectedRow = jTablePhieuNhap.getSelectedRow();
                    if (selectedRow != -1) {
                        // Hiển thị thông báo nếu hàng đã được chọn
                        JOptionPane.showMessageDialog(null, "You do not have permission to edit the Purchase Order ID", "Warning", JOptionPane.WARNING_MESSAGE);
                        jTextMaNhap.setText(maNhap);
                        jTextMaNhap.setEditable(false);
                    }
                }
            });

        }
    }//GEN-LAST:event_jTablePhieuNhapMouseClicked

