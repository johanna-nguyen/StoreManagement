    // Load bảng phiếu nhập
    public void loadPhieuNhapData() throws ClassNotFoundException {
        List<PhieuNhap> phieuNhapList = phieuNhapController.getAllPhieuNhap();
        DefaultTableModel model = (DefaultTableModel) jTablePhieuNhap.getModel();
        model.setRowCount(0);

        for (PhieuNhap phieuNhap : phieuNhapList) {
            model.addRow(new Object[]{
                phieuNhap.getMaNhap(),
                phieuNhap.getMaSP(),
                phieuNhap.getSoLuong(),
                phieuNhap.getGiaNhap(),
                phieuNhap.getNgayNhap(),
                phieuNhap.getMaNCC()
            });
        }

    }

